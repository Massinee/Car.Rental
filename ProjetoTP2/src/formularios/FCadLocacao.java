    public Locacao execute(){
        cmbCarros.removeAllItems();
        cmbClientes.removeAllItems();        
        
        for(Carro car : lstCarros){
            cmbCarros.addItem(car.getModelo() + " - " + car.getPlaca());
        }
        
        for(Clientes cli : lstClientes){
            cmbClientes.addItem(cli.getNome() + " - " + cli.getCPFcnpj());
        }
        
        cmbCarros.repaint();
        cmbClientes.repaint();
        
        this.setVisible(true);
        return this.locTemp;
    }
    
