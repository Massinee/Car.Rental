    private void btnSalvarLocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarLocActionPerformed
        try{
            
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy" + "HH:mm");
            Locacao loc = new Locacao();
            
            String hr = txtHoraLoc.getText();
            
            Clientes cli = ClientesDados.lstClientes.get(cmbClientes.getSelectedIndex());
            Carro car = CarrosDados.lstCarros.get(cmbCarros.getSelectedIndex());
            
            boolean ver = true;
            
            if(LocacaoDados.lstLocacao.size() > 0){
                ver = true;
                
                for(Locacao locTeste : LocacaoDados.lstLocacao){

                    if(car == locTeste.getCarro() && locTeste.getDtEntrega() == null){
                        JOptionPane.showMessageDialog(this, "Este carro não está disponível!");
                        ver = false;
                    }
                }
            }
            if("  /  /    ".equals(txtDtLoc.getText())){
                JOptionPane.showMessageDialog(this,
                            "Insira a data de entrega.", "Atenção!", JOptionPane.WARNING_MESSAGE);
                ver=false;               
            }
            if("  :  ".equals(txtDtLoc.getText())){
                JOptionPane.showMessageDialog(this,
                            "Insira a hora de entrega.", "Atenção!", JOptionPane.WARNING_MESSAGE);
                ver=false;               
            }
            if(ver){
                loc.setCodigo(loc.getCodigo());
                loc.setCliente(cli);
                loc.setCarro(car);
                loc.setDtLocacao(sdf.parse(txtDtLoc.getText() + hr));
                loc.setDtEntrega(null);
                loc.setQtdDiasLocados(loc.getQtdDiasLocados());
                loc.setValorTotalLocacao(loc.getValorTotalLocacao());
                
            this.locTemp = loc;
            setVisible(false);
            }
            
        } catch(Exception ex){
            JOptionPane.showMessageDialog(this, 
                    "Não foi possível criar locacação \n\n" + ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarLocActionPerformed

