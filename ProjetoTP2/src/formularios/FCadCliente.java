    public Clientes execute(Clientes c) throws ParseException{
        this.alterar = true;
        this.clienteTemp = c;
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        
        if( c instanceof PessoaFisica){
            rdbPJ.setEnabled(false);
            rdbPF.setSelected(true);
            rdbPF.setEnabled(false);
            
            lblCPFcnpj.setText("CPF");

            txtCPFcnpj.setText(c.getCPFcnpj());
            txtDtNasc.setText(sdf.format(c.getDtNasc()));
            
            lblDesconto.setVisible(false);
            txtDesconto.setVisible(false);
        
        }else{
            rdbPF.setEnabled(false);
            rdbPJ.setSelected(true);
            rdbPJ.setEnabled(false);
            
            lblCPFcnpj.setText("CNPJ");
            
            MaskFormatter formato = new MaskFormatter("##.###.###/####-##");
            DefaultFormatterFactory mascara = new DefaultFormatterFactory(formato);
            txtCPFcnpj.setFormatterFactory(mascara);
            
            txtCPFcnpj.setText(c.getCPFcnpj());
            txtDtNasc.setText(c.getDtNascPJ());
            
            lblDesconto.setVisible(true);
            txtDesconto.setVisible(true);
            lblDtNasc.setVisible(false);
            txtDtNasc.setVisible(false);
        }
                
        txtNome.setText(c.getNome());
        txtEndereco.setText(c.getEndereco());
        txtCNH.setText(c.getCNH());
        txtDesconto.setText(Double.toString(c.getDesconto()));

        setVisible(true);
        return clienteTemp;
    }

