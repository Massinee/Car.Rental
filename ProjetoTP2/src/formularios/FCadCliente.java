    private void btnSalvarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarClienteActionPerformed
        try{
            
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            Clientes cliente = null;
            
            boolean ver = true;
            
            if(rdbPF.isSelected()){
                ver= true;
                
                if(this.alterar){
                    cliente = this.clienteTemp;
                }else{
                    cliente = new PessoaFisica();
                }
                
                PessoaFisica pf = (PessoaFisica) cliente;
                
                if("   .   .   -  ".equals(txtCPFcnpj.getText())){
                    
                    JOptionPane.showMessageDialog(this,
                            "Insira um CPF.", "Atenção!", JOptionPane.WARNING_MESSAGE);
                    ver= false;
                }
                if(ver){
                    pf.setCpf(txtCPFcnpj.getText());
                    pf.setCnh(txtCNH.getText());
                    pf.setDtNascimento(sdf.parse(txtDtNasc.getText()));
                    
                    cliente.setNome(txtNome.getText());
                    cliente.setEndereco(txtEndereco.getText());
                    
                    this.clienteTemp = cliente;
                    setVisible(false);
                }
            }
            
            if(rdbPJ.isSelected()){
                ver = true;
                
                if(this.alterar){
                    cliente = this.clienteTemp;
                }else{
                    cliente = new PessoaJuridica();
                }
                
                PessoaJuridica pj = (PessoaJuridica) cliente;
                
                if("  .   .   /    -  ".equals(txtCPFcnpj.getText())){
                    JOptionPane.showMessageDialog(this,
                            "Insira um CNPJ.", "Atenção!", JOptionPane.WARNING_MESSAGE);
                    ver = false;
                }
                
                if(ver){
                    pj.setCnpj(txtCPFcnpj.getText());
                    pj.setCnhResponsavel(txtCNH.getText());
                    pj.setDescontoLocacao(Double.parseDouble(txtDesconto.getText()));
                    
                    cliente.setNome(txtNome.getText());
                    cliente.setEndereco(txtEndereco.getText());
                    
                    this.clienteTemp = cliente;
                    setVisible(false);
                }               
            }
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, 
                    "Não foi possível criar/alterar cliente \n\n" + ex.getMessage(), "ERRO!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarClienteActionPerformed

